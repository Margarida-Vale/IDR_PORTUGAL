st.title("🧭 Sobre o Projeto IDR")

st.markdown("""
O projeto **Índice Digital Regional (IDR)** foi concebido como uma ferramenta de investigação e desenvolvimento para avaliar a maturidade tecnológica das regiões portuguesas.  
Baseia-se em três eixos de análise principais:

- **Contexto:** condições estruturais e socioeconómicas (escolaridade, demografia, poder de compra);
- **Utilização:** comportamento digital de cidadãos e empresas;
- **Oferta:** infraestrutura tecnológica e capacidade institucional.

O objetivo central é gerar conhecimento acionável que auxilie políticas públicas e estratégias de desenvolvimento digital regional.
""")
